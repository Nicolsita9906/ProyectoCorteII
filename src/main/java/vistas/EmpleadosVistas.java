    private void cambiarButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
       String nombreEmp = nom.getText();
       TipoDeDocumento tipDocumento = (TipoDeDocumento) tDoc.getSelectedItem();
       int document = tipDocumento.getValor();
       String numeroDoc = nDoc.getText();
       DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
       String fechaTexto = ffIng.getText();
       LocalDateTime fechaEmp = LocalDateTime.parse(fechaTexto, formatter);

       String contrasenia = contraseniaaa.getText();

       int buscar = Integer.parseInt(busCod.getText());
       if (contrasenia == null){
        control.modificarEmpleado(buscar, new Empleado (nombreEmp, document, numeroDoc, null, fechaEmp));
       }else {
        control.modificarEmpleado(buscar, new Empleado (nombreEmp, document, numeroDoc, contrasenia, fechaEmp));
       }
       cargarTabla();
       limpiarCampos();
    }                                             

    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
    	String nombreEmp = nom.getText();
        TipoDeDocumento tipDocumento = (TipoDeDocumento) tDoc.getSelectedItem();
        int document = tipDocumento.getValor();
        String numeroDoc = nDoc.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDateTime fechaProd = LocalDateTime.parse(ffIng.getText(),formatter );
        String contrasenia = contraseniaaa.getText();
        
        control.agregarEmplado(new Empleado (nombreEmp, document, numeroDoc, contrasenia, fechaProd));
        cargarTabla();
        limpiarCampos();
    }                                             

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {                                               
        int buscar = Integer.parseInt(busCod.getText());
        control.eliminarEmpleado(buscar);
        cargarTabla();
        limpiarCampos();
    }                                              

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {                                             
        int buscar = Integer.parseInt(busCod.getText());
    	Empleado busqueda = control.buscarEmpleado(buscar);
    	
    	if (busqueda != null) {
    		nom.setText(busqueda.getNombreEmpleado());
    		tDoc.setSelectedItem(busqueda.getTipoDocumento());
    		nDoc.setText(busqueda.getNumeroDocuemento());
    		ffIng.setText(String.valueOf(busqueda.getFechaIngreso()));	

    		 try {
                 throw new NotificarExito(exito);
             } catch (NotificarExito e) {
                 exito.setVisible(true);
             }
    		
    	} else {
              try {
             throw new CodigoNoEncontrado(noEncontrado);
         } catch (CodigoNoEncontrado e) {
         	noEncontrado.setVisible(true);
         }	
    	}
    	
    }                                            

